<template>
  <div class="langContain">
    <span
      v-for="(item, i) in allLanguages"
      :key="i"
      :class="{ lang: true, _active: langActive === item }"
      @click.stop="setLanguage(item)"
    >
      {{ item }}
    </span>
  </div>
</template>

<script>
export default {
  name: 'Languages',
  props: ['langActive'],
  data() {
    return {
      allLanguages: ['eng', 'ua'],
    };
  },
  methods: {
    setLanguage(newLang) {
      this.$emit('setLanguage', newLang);
    },
  },
};
</script>

